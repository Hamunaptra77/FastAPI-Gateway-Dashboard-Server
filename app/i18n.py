def resolve_language(request: Request) -> tuple[str, bool]:
    query_lang = request.query_params.get("lang", "").lower()
    if query_lang in SUPPORTED_LANGUAGES:
        return query_lang, True

    cookie_lang = request.cookies.get("lang", "").lower()
    if cookie_lang in SUPPORTED_LANGUAGES:
        return cookie_lang, False

    accept = request.headers.get("accept-language", "")
    for part in accept.split(","):
        token = part.strip().split(";")[0].lower()
        if not token:
            continue
        short = token.split("-")[0]
        if short in SUPPORTED_LANGUAGES:
            return short, False

    return DEFAULT_LANGUAGE, False


def translator(language: str) -> Callable[[str], str]:
    lang_dict = TRANSLATIONS.get(language, TRANSLATIONS[DEFAULT_LANGUAGE])
    fallback = TRANSLATIONS["en"]

    def _t(key: str) -> str:
        return lang_dict.get(key, fallback.get(key, key))

    return _t


def apply_language_cookie(response: Response, language: str, should_set: bool) -> None:
    if should_set:
        response.set_cookie(
            "lang",
            language,
            max_age=31536000,
            httponly=True,
            samesite="lax",
            secure=settings.session_cookie_secure_enabled(),
        )
